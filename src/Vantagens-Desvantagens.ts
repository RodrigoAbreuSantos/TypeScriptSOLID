/*
Vantagens e desvantagens do S.O.L.I.D.

Vantagens:
- Código modular ==> Cada modulo faz alguma coisa especifica
- Código reutilizável (D.R.Y - Don't repeat yourself)
- Código testável, baixo acoplamento ==> classes dependendo de protocolos e não de outras classes
- Baixo acoplamento e alta coesão
- Código expansível
- Separations of concerns (Separação de conceitos) ==> Ao inves de fazer tudo em uma classe, sepraou em varias classes
- Fácil manutenção
Desvantagens:
- Complexidade
- Quantidade de código digitado aumenta
- Tempo de desenvolvimento aumenta bastante
- Tenha cuidados com: YAGNI, KISS (You aren't gonna need it, Keep it simple, stupid!)
*/

